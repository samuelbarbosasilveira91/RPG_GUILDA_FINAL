CREATE MATERIALIZED VIEW operacoes.mv_painel_tatico_missao AS
 WITH atraso AS MATERIALIZED (
         SELECT operacoes.atrasar_consulta(4) AS ok
        )
 SELECT m.id AS missao_id,
    m.titulo,
    m.status,
    m.nivel_perigo,
    m.organizacao_id,
    count(pm.aventureiro_id) AS total_participantes,
    (avg(a.nivel))::numeric(10,2) AS nivel_medio_equipe,
    sum(pm.recompensa_ouro) AS total_recompensa,
    sum(
        CASE
            WHEN (pm.destaque = true) THEN 1
            ELSE 0
        END) AS total_mvps,
    sum(
        CASE
            WHEN (c.aventureiro_id IS NOT NULL) THEN 1
            ELSE 0
        END) AS participantes_com_companheiro,
    max(pm.data_registro) AS ultima_atualizacao,
    (((avg(a.nivel) * (2)::numeric) + ((count(pm.aventureiro_id) * 5))::numeric) + (sum(
        CASE
            WHEN pm.destaque THEN 10
            ELSE 0
        END))::numeric) AS indice_prontidao
   FROM ((((operacoes.missao m
     LEFT JOIN operacoes.participacao_missao pm ON ((pm.missao_id = m.id)))
     LEFT JOIN operacoes.aventureiro a ON ((a.id = pm.aventureiro_id)))
     LEFT JOIN operacoes.companheiro c ON ((c.aventureiro_id = a.id)))
     CROSS JOIN atraso)
  GROUP BY m.id, m.titulo, m.status, m.nivel_perigo, m.organizacao_id;


ALTER MATERIALIZED VIEW operacoes.mv_painel_tatico_missao OWNER TO postgres;


